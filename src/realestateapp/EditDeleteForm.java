    // Μέθοδος φόρτωσης των δεδομένων χρηστών και ακινήτων στον πίνακα
    public void loadUsersPropertiesData() {
        DefaultTableModel model = (DefaultTableModel) tblEditUsersProperties.getModel();
        model.setRowCount(0); // Διαγραφή των υπαρχόντων δεδομένων
        // Προσθήκη χρηστών και ακινήτων στον πίνακα αν είναι admin ή αν είναι ο τρέχων χρήστης
        for (User user : Database.users) 
        {
        // Αν είναι admin ή αν το username ταιριάζει με τον logged user, εμφανίζει τα δεδομένα
            if (isAdmin || user.getUsername().equals(adminUsername)) 
            {
                ImageIcon icon = null;
                if (user.getImagePath() != null && !user.getImagePath().isEmpty()) 
                {
                    icon = new ImageIcon(new ImageIcon(user.getImagePath()).getImage().getScaledInstance(80, 60, Image.SCALE_SMOOTH));
                } 
                else 
                {
                    icon = new ImageIcon("default.png"); // Default εικόνα αν δεν υπάρχει
                }
                model.addRow(new Object[]{
                    user.getUsername(),
                    user.getInitialPrice(),
                    user.getFinalPrice(),
                    icon
                });
            }
        }
        if (tblEditUsersProperties.getColumnCount() > 3) 
        {
        tblEditUsersProperties.getColumnModel().getColumn(3).setCellRenderer(new ImageRenderer());
        }
    }

