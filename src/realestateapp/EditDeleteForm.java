    // Μέθοδος διαχείρισης του κουμπιού "Επεξεργασία"
    private void btnEditUserPropertyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditUserPropertyActionPerformed
        if (tblEditUsersProperties == null) 
        {
            System.out.println("tblEditUsersProperties is null!");
            return; 
        }
        int selectedRow = tblEditUsersProperties.getSelectedRow();
        if (selectedRow == -1) 
        {
            JOptionPane.showMessageDialog(this, "Παρακαλώ επιλέξτε έναν χρήστη για επεξεργασία.", "Σφάλμα", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else
        {
            // Ανάκτηση δεδομένων από την επιλεγμένη γραμμή
            String username = (String) tblEditUsersProperties.getValueAt(selectedRow, 0);
            double initialPrice = (double) tblEditUsersProperties.getValueAt(selectedRow, 1);
            double finalPrice = (double) tblEditUsersProperties.getValueAt(selectedRow, 2);
            ImageIcon icon = null;
            Object cellValue = tblEditUsersProperties.getValueAt(selectedRow, 3);
            if (cellValue instanceof ImageIcon) 
            {
                icon = (ImageIcon) cellValue;
            }
            String imagePath="default.png";
            for (User user : Database.users)
            {
                if(user.getUsername().equals(username))
                {
                    imagePath = (user.getImagePath() != null && !user.getImagePath().isEmpty()) ? user.getImagePath() : "default.png";
                }
            }
            // Άνοιγμα της φόρμας επεξεργασίας
            EditUserPropertyForm editForm = new EditUserPropertyForm(this, username, initialPrice, finalPrice,imagePath);
            editForm.setVisible(true);  
        }
    }//GEN-LAST:event_btnEditUserPropertyActionPerformed
    // Μέθοδος διαχείρισης του κουμπιού "Διαγραφή"
    private void btnDeleteUserPropertyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteUserPropertyActionPerformed
        int selectedRow = tblEditUsersProperties.getSelectedRow();
        if(selectedRow == -1)
        {
            JOptionPane.showMessageDialog(this, "Επιλέξτε μια εγγραφή!", "Σφάλμα", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Είστε σίγουρος για τη διαγραφή;", "Επιβεβαίωση", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION)
        {
            String username = (String) tblEditUsersProperties.getValueAt(selectedRow, 0);
            Database.users.removeIf(user -> user.getUsername().equals(username));// Διαγραφή χρήστη από τη λίστα
            DefaultTableModel model = (DefaultTableModel) tblEditUsersProperties.getModel();
            model.removeRow(selectedRow);// Διαγραφή της αντίστοιχης γραμμής από τον πίνακα
        }
    }//GEN-LAST:event_btnDeleteUserPropertyActionPerformed
